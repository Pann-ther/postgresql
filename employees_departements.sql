
-- Creation des tables
CREATE TABLE dept (
    ND INTEGER PRIMARY KEY,
    NOM VARCHAR(14),
    VILLE VARCHAR(13)
);

CREATE TABLE emp (
    NE INTEGER PRIMARY KEY,
    NOM VARCHAR(10) NOT NULL,
    JOB VARCHAR(9),
    DATEMB DATE,
    SAL NUMERIC(7,2),
    COMM NUMERIC(7,2),
    ND INTEGER NOT NULL,
    NEchef INTEGER,
    FOREIGN KEY(ND) REFERENCES dept(ND),
    FOREIGN KEY(NEchef) REFERENCES emp(NE)
);

